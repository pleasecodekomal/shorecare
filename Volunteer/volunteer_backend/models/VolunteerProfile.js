import mongoose from 'mongoose';

const volunteerProfileSchema = new mongoose.Schema({
  username: {
    type: String,
    required: true,
    unique: true,
    trim: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
    lowercase: true,
  },
  phone: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
  isVerified: {
    type: Boolean,
    default: false,
  }
}, { collection: 'volunteer_profiles', timestamps: true });

export default mongoose.model('VolunteerProfile', volunteerProfileSchema);
